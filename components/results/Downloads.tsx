'use client';

import JSZip from 'jszip';
import SectionHeader from '@/components/ui/SectionHeader';
import Button from '@/components/ui/Button';
import { text } from '@/constants/text';
import { downloadMarkdown, downloadZipContent } from '@/lib/utils/markdown';
import { BrandKit } from '@/lib/types';

interface DownloadsProps {
  brandingMd: string;
  copywriteMd: string;
  kit: BrandKit;
  brandName: string;
}

function buildColorsCSS(colors: BrandKit['colors']): string {
  return `:root {
  --color-primary: ${colors.primary};
  --color-secondary: ${colors.secondary};
  --color-accent: ${colors.accent};
  --color-dark: ${colors.dark};
  --color-light: ${colors.light};
}
`;
}

function buildColorsJSON(colors: BrandKit['colors']): string {
  return JSON.stringify(colors, null, 2);
}

function buildSeoJSON(seo: BrandKit['seo']): string {
  return JSON.stringify(seo, null, 2);
}

function buildLogoPromptTxt(prompt: string): string {
  return `LOGO GENERATION PROMPT\n${'='.repeat(40)}\n\nUse this prompt in Midjourney, DALL-E, or any AI image generator:\n\n${prompt}\n`;
}

function buildReadme(brandName: string): string {
  return `# ${brandName} — Brand Kit

This ZIP contains your complete brand identity kit generated by DigiFootprint.

## Contents

| File | Description |
|------|-------------|
| \`branding.md\` | Full brand guide — name, colors, fonts, tone, personality |
| \`copywrite.md\` | Website copy — hero, tagline, value props, CTAs, about |
| \`colors.css\` | CSS custom properties for your color palette |
| \`colors.json\` | Color palette as JSON |
| \`seo.json\` | SEO keywords, meta tags, and OG tags |
| \`logo-prompt.txt\` | AI image generation prompt for your logo |

## Quick Start

1. Use \`colors.css\` in your project for consistent colors
2. Copy fonts from \`branding.md\` and load them from Google Fonts
3. Paste \`logo-prompt.txt\` into Midjourney or DALL-E
4. Use \`seo.json\` for your meta tags

---
Generated by [DigiFootprint](https://digifootprint.vercel.app)
`;
}

async function handleDownloadZip(kit: BrandKit, brandName: string, brandingMd: string, copywriteMd: string) {
  const zip = new JSZip();
  const folder = zip.folder(brandName.toLowerCase().replace(/[^a-z0-9]/g, '-') + '-brand-kit')!;

  folder.file('README.md', buildReadme(brandName));
  folder.file('branding.md', brandingMd);
  folder.file('copywrite.md', copywriteMd);
  folder.file('colors.css', buildColorsCSS(kit.colors));
  folder.file('colors.json', buildColorsJSON(kit.colors));
  folder.file('seo.json', buildSeoJSON(kit.seo));
  folder.file('logo-prompt.txt', buildLogoPromptTxt(kit.logoPrompt));

  const base64 = await zip.generateAsync({ type: 'base64' });
  const filename = `${brandName.toLowerCase().replace(/[^a-z0-9]/g, '-')}-brand-kit.zip`;
  await downloadZipContent(base64, filename);
}

export default function Downloads({ brandingMd, copywriteMd, kit, brandName }: DownloadsProps) {
  return (
    <div>
      <SectionHeader
        title={text.results.downloads.title}
        description={text.results.downloads.description}
      />

      {/* ZIP download — primary action */}
      <Button
        onClick={() => handleDownloadZip(kit, brandName, brandingMd, copywriteMd)}
        fullWidth
        className="mb-4"
      >
        <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
        </svg>
        Download everything as ZIP
      </Button>

      {/* Individual files */}
      <div className="flex flex-col sm:flex-row gap-3">
        <Button
          variant="secondary"
          onClick={() => downloadMarkdown(brandingMd, 'branding.md')}
          className="flex-1"
        >
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 10v6m0 0l-3-3m3 3l3-3m2 8H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
          </svg>
          {text.results.downloads.branding}
        </Button>
        <Button
          variant="secondary"
          onClick={() => downloadMarkdown(copywriteMd, 'copywrite.md')}
          className="flex-1"
        >
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 10v6m0 0l-3-3m3 3l3-3m2 8H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
          </svg>
          {text.results.downloads.copywrite}
        </Button>
      </div>
    </div>
  );
}
